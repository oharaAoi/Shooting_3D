//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　初期化処理
//////////////////////////////////////////////////////////////////////////////////////////////////

void MobEnemy::Init(std::vector<Model*> models) {
	BaseEnemy::Init(models);

	worldTransforms_[EnmeyParts::Enmey_Body].parent_ = &worldTransform_;
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　更新処理
//////////////////////////////////////////////////////////////////////////////////////////////////

void MobEnemy::Update() {
	worldTransform_.translation_.z = 4;
	BaseEnemy::Update();
}

